package tcase

import (
	"path/filepath"
	"reflect"
	"testing"

	"github.com/go-spatial/geom"
	"github.com/go-spatial/geom/internal/parsing"
)

type tcase struct {
	filename string
	cases    []C
}

func TestParse(t *testing.T) {

	fn := func(tc tcase) func(*testing.T) {
		return func(t *testing.T) {
			cases, err := ParseFile(tc.filename)
			if err != nil {
				t.Errorf("error, expected nil got %v", err)
			}
			if len(cases) != len(tc.cases) {
				t.Errorf("number of cases, expected %v got %v", len(tc.cases), len(cases))
				return
			}
			for i, tcase := range tc.cases {
				acase := cases[i]
				if acase.Desc != tcase.Desc {
					t.Errorf("Desc, expected %v got %v", tcase.Desc, acase.Desc)
				}
				if !reflect.DeepEqual(tcase.Expected, acase.Expected) {
					t.Errorf("Expected, expected %#v got %#v", tcase.Expected, acase.Expected)
				}
				if !reflect.DeepEqual(tcase.Bytes, acase.Bytes) {
					t.Errorf("Bytes, expected %v got %v", tcase.Bytes, acase.Bytes)
				}
			}
		}
	}
	tests := map[string]tcase{
		"point": {
			cases: []C{
				{
					Desc:     "This is a simple test",
					Expected: geom.Point{2, 4},
					Bytes:    []byte{0x00, 0x00, 0x00, 0x00, 0xaf, 0x00, 0xaf, 0x0c, 0xd0, 0x0d, 0xac, 0x00, 0xDE, 0xAF, 0xD0, 0x0d, 0xac, 0xff, 0x00, 0x00},
				},
				{
					Desc:     "This is a simple test",
					Expected: geom.Point{2, 4},
					Bytes:    []byte{0x00, 0x00, 0x00, 0x00, 0xaf, 0x00, 0xaf, 0x0c, 0xd0, 0x0d, 0xac, 0x00, 0xDE, 0xAF, 0xD0, 0x0d, 0xac, 0xff, 0x00, 0x00},
				},
			},
		},
		"polygon": {
			cases: []C{
				{
					Desc:         "Simple polygon with two rings.",
					DescPosition: parsing.Position{Row: 7, Col: 1},
					Expected: geom.Polygon{
						{{35, 10}, {45, 45}, {15, 40}, {10, 20}},
						{{20, 30}, {35, 35}, {30, 20}},
					},
					Bytes: []byte{
						0x01,                   // BOM
						0x03, 0x00, 0x00, 0x00, // Type
						0x02, 0x00, 0x00, 0x00, // Number of Rings 2
						0x05, 0x00, 0x00, 0x00, // Number of points in ring 5
						0x00, 0x00, 0x00, 0x00, 0x00, 0x80, 0x41, 0x40, // X1 35
						0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x24, 0x40, // Y1 10
						0x00, 0x00, 0x00, 0x00, 0x00, 0x80, 0x46, 0x40, // X2 45
						0x00, 0x00, 0x00, 0x00, 0x00, 0x80, 0x46, 0x40, // Y2 45
						0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x2E, 0x40, // X3 15
						0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x44, 0x40, // Y3 40
						0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x24, 0x40, // X4 10
						0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x34, 0x40, // Y4 20
						0x00, 0x00, 0x00, 0x00, 0x00, 0x80, 0x41, 0x40, // X5 35
						0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x24, 0x40, // Y5 10
						0x04, 0x00, 0x00, 0x00, // Number of points in ring 4
						0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x34, 0x40, // X1 20
						0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x3E, 0x40, // Y1 30
						0x00, 0x00, 0x00, 0x00, 0x00, 0x80, 0x41, 0x40, // X2 35
						0x00, 0x00, 0x00, 0x00, 0x00, 0x80, 0x41, 0x40, // Y2 35
						0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x3E, 0x40, // X3 30
						0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x34, 0x40, // Y3 20
						0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x34, 0x40, // X4 20
						0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x3E, 0x40, // Y4 30
					},
				},
			},
		},
	}
	for name, tc := range tests {
		if tc.filename == "" {
			tc.filename = filepath.Join("testdata", name+".tcase")
		}
		t.Run(name, fn(tc))
	}
}
